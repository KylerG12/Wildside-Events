function getStateAbbr(state) {
  return states[state] || null; 
}

function populateStateList(lastState){
  var stateList = $('#states');
  
  
  stateList.empty();

  $.each(states, function(state) {
    var opt =$('<option>', {
      value: state,
      text: state
    });

    // select last state
    if (lastState == state){
      opt.prop('selected', true);
    }

    stateList.append(opt);

  });
}

